fs = 1000;
t = 0:1/fs:0.1;
x1 = sin(2*pi*100*t);
x2 = sin(2*pi*250*t);
[b,a] = ellip(8, 0.7, 120, 0.3);
y1 = filter(b,a,x1);
y2 = filter(b,a,x2);
subplot(2,1,1);
plot(t,x2,'r', t, y2, ':');
ylabel('Amplitude');
xlabel('Tempo (s)');
title('Sinal 250 Hz');
